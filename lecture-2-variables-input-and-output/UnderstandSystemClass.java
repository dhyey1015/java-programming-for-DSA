class UnderstandSystemClass{
    public static void main(String args[]){
        //system is a class in java and out is an instance of some other print related class
        //which contains print or println function
        //println is used to print things in next line
        System.out.println("Hello, World!!--------using println"); 
        //print is used when you don't want your output to be print in next line
        System.out.print("Hello World----------using print");
        //"\n" for new line
        System.out.print("Hello World---------using backslash-n \n");
        System.out.println("hello");

    }
}